   <h2>Biographie</h2> 
<p>Il y a fort longtemps  j'étais  le leader d'une troupe de gade royaux d'un certain royame de West Blue.</br>
Par la suite j'ai décider de rejoindre l'équipage des Rumbar Pirates en tant que Second.</br>
J'ai finalement fini par trouvé un fruit du démon quel ne fut pas mon désarroie quand je me suis rendu compte qu'absolument rien n'avait changer aprés l'avoir manger ...</br> a part le fait que je ne puisse plus nager,
quelle malédiction.
Plus tard avec mon nouvel équipage,</br> nous avons fait la rencontre d'un jeune balaineau qui dans notre périple sur des mers de plus en plus dangereuse,</br> n'arréta pas de nous suivre sans relâche.
Il finit par la suite par intégrer notre équipage et par la même occasion de le baptiser Laboon.</br>
Une fois arrivé a Grand Line Nous n'avons malheureusement pas eu d'autre choix que de laisser Laboon au Cap des Jumeaux en attend notre retour qui ne devait pas durée plus de trois années.</br>
Malheureusement quelque temps aprés la  traversée de Grand Line le malheur s'abbati sur notre équipage,</br> nous fumes toucher par une épidémie qui emporte la majorité de l'équipage vers d'autre cieux , le capitaine Yorki ni échappa pas ...</br>
sa derniére decision fut de me laisser le commandemant des survivants.</br>
par la suite nous n'avons pas eu beaucoup plus de chance, une intense bataille naval nous attendait, l'équipage n'y a pas survécu.</br>
Avant de mourrir j'ai enregistrer une derniére chanson sur un Tone Dial tout en me remémorant les moments passés avec mes défunts camarades.</br>
Ce ne fut qu'aprés ma mort que je me rendis compte de la capacité que j'avais acquis avec mon fruit du démon, le fruit de la résurrection.</br>
le probléme fut qu'étant une simple ame je perdis une année entiére pour retrouvé mon navire ainsi que mon anciens corp ou dumoins ce qu'il en restait.</br>
Par la suite l'équipage du chapeau qui m'a aider a récuper mon ombre que Gecko Moria m'avait voler.</br>
J'ai finalement fini par intégrer l'équipage de luffy en tant que musicien et second épéiste.</br>
Quelque temp aprés cela tout en restant membre de l'équipage du chapeau de paille, j'ai fait mes débuts sur la scéne en tant Soul King.</br>
mais ce ne fut qu'une bref période avant de repartir a la recherche du trésors de Gol D. Roger et d'aider Luffy a devenir le roi des Pirates. </br>
</p>
