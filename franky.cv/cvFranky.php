<h2 id = "Compétence">• Aptitudes et Compétences</h2>
<p> Franky est un équipier compétent ainsi qu'un atout précieux pour  l’Equipage du Nouveau Monde.
 Il a construit lui même le navire de l'équipage, et en tant que charpentier officiel, 
 il est chargé de le maintenir en excellent état, ainsi que les autres véhicules, 
 instruments ou armes que les membres du Nouveau Monde peuvent utiliser.
  Franky possède également une super ouïe depuis sa naissance. </p>

  <h2 id = "Compétence">• Expertise en construction et ingénierie</h2>
  <p> Franky est un maître artisan hors pair, travaillant le bois et le métal comme personne, et est le charpentier de l'équipage.
   Ses compétences en tant que charpentier sont plus qu'honorables et reconnues.
   Dans sa jeunesse, il a participé à la production du premier Train des mers, le Puffing Tom, 
   tout en construisant dans son temps libre ses Battles Franky.
    Il a ensuite passé plusieurs années en tant que désosseur de Navires avec la Franky Family, 
    ce qui n'a rien enlevé à son talent puisqu'il a ensuite 
   construit le Thousand Sunny et toutes ses options en seulement 5 jours avec l'aide de
    Yokozuna, Icebarg, Pauly, Tileston et Peeply Lulu.
   <br/>
   Il a démontré à plusieurs reprises son efficacité à construire des objets avec une très grande vitesse.
   Il sait également s'adapter à de nouveaux styles d'architecture. 
   Franky a été capable de diriger une équipe de charpentiers du Pays des Wa pour restaurer les navires.
   Malgré sa nature sauvage et imprudente, Franky a démontré qu'il était capable de réfléchir efficacement en situation de crise. </p>
